    funcionario (
        pk_funcionario NUMBER (10) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
        nome VARCHAR2 (100) NOT NULL,
        gerente NUMBER (1) DEFAULT 0 NOT NULL,
        pk_gerente NUMBER DEFAULT NULL REFERENCES funcionario
    );

