CREATE TABLE
    metas (
        pk_metas NUMBER (10) GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
        pk_funcionario NUMBER (10) REFERENCES funcionario,
        categoria VARCHAR2 (30) NOT NULL,
        data DATE NOT NULL,
        quantidade NUMBER NOT NULL,
        nota NUMBER CHECK (nota <= 100),
        data_avaliacao DATE,
        nota_final VARCHAR2 (5)
